function chooseAnimation (velx: number, vely: number) {
    if (velx > 0) {
        animation.setAction(mySprite, ActionKind.WalkRight)
    } else {
        if (velx < 0) {
            animation.setAction(mySprite, ActionKind.WalkLeft)
        } else {
            if (vely > 0) {
                animation.setAction(mySprite, ActionKind.WalkFront)
            } else {
                if (vely < 0) {
                    animation.setAction(mySprite, ActionKind.WalkBack)
                } else {
                    animation.stopAnimation(animation.AnimationTypes.All, mySprite)
                }
            }
        }
    }
}
